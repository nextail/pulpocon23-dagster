reset='\033[0m'
bold='\033[1m'
italic='\033[3m'
underline='\033[4m'
red='\033[91m'
green='\033[92m'
yellow='\033[93m'
blue='\033[94m'
purple='\033[95m'
cyan='\033[96m'

# Welcome message with ANSI art
echo -e "${purple}${bold}                                                                        ,       ${reset}"
echo -e "${purple}${bold}                                                        *******************     ${reset}"
echo -e "${purple}${bold}                                                       ***********************, ${reset}"
echo -e "${purple}${bold}                                                    ****************************${reset}"
echo -e "${purple}${bold} &&&&&# &&.   && &&,    &&&&&&   &&&&&&,          ************       ,**********${reset}"
echo -e "${purple}${bold} &&  &&,&&.   && &&,    &&  &&&&&%  &&&&       **********.   ********    *****, ${reset}"
echo -e "${purple}${bold} &&&&#  &&&  &&& &&,    &&&&@  &&&&  &&/       ********  , **************     ${reset}"
echo -e "${purple}${bold} &&       &&&&   &&&&&& &&.    . &&&&          *******   ****       *****     ${reset}"
echo -e "${purple}${bold}                                               ******  .***   //     *****    ${reset}"
echo -e "${purple}${bold} && & #&..&& &@ &&   ////////  ////////        ******   ***  //////  ,****    ${reset}"
echo -e "${purple}${bold} && ( &&@ &&.& &&&       .///       (//       *******   ***,         *****    ${reset}"
echo -e "${purple}${bold}                     .///*        /////         ******    *******   *****     ${reset}"
echo -e "${purple}${bold}                    ,////////       .///         *******         *******      ${reset}"
echo -e "${purple}${bold}                               ///////            ********************        ${reset}"
echo -e "${purple}${bold}                                                      *************           ${reset}"


# Print the welcome message
echo -e "${bold}${underline}${blue}Get ready for an exciting time!${reset}"
